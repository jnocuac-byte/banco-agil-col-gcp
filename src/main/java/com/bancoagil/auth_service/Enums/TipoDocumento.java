package com.bancoagil.auth_service.Enums;

public enum TipoDocumento {

    // Cédula de Ciudadanía (Colombia)
    CC,
    
    // Cédula de Extranjería (Colombia)
    CE,
    
    // Tarjeta de Identidad (Colombia, para menores de edad)
    TI,
    
    // Pasaporte
    PASAPORTE,
    
    // Documento Nacional de Identidad (común en varios países, ej. Argentina, España)
    DNI,
    
    // Otro tipo de identificación
    OTRO
}
